function encontrarMayor(a, b, c) {
    return Math.max(a, b, c);
}
// Ejemplo de uso:
let num1 = 10, num2 = 25, num3 = 15;
let mayor = encontrarMayor(num1, num2, num3);
console.log(`El mayor de ${num1}, ${num2} y ${num3} es: ${mayor}`);
